package Assignment;

public class IfElseAssignment {

	public static void main(String[] args) {
		// If my grade is A I got above 90% marks else I got below 90%
		
		char grade='B';
		
		if (grade=='A')
		{
			System.out.println("I got above 90% marks");
			
		}
		else
		{

			System.out.println("I got below 90% marks");
		}	
		
		

	}

}
