package for_loop;

public class while_loop {

	public static void main(String[] args) {
		// I want 4 table
		//initilization-->  4
		//condition-->  <=40
		//updation-->  i=i+4
		
		//initilization
		int i=4;
		
		while (i<=40) //condition
		{
			System.out.println(i);
			i=i+4;
		}
		System.out.println("======================================");
		
		
		int b=50;
		while (b>=5)
		{
		System.out.println(b);
		b=b-5;
		}
		
	}

}
