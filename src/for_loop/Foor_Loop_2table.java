package for_loop;

public class Foor_Loop_2table {

	public static void main(String[] args) {
		//i want to print 2 table;
		//Initialization-->2
		//Condition--> <=20
		//Updation-->  a=a+2
		
		for(int a=2;a<=20;a=a+2)
		{
			System.out.println(a);
		}
		System.out.println("=====================");

		
		//i want to print 2 table;
				//Initilization-->4
				//Condition--> <=40
				//Updation-->  b=b+4
		for(int b=4;b<=40;b=b+4)
		{
			System.out.println(b);
		}
		
	}

}
