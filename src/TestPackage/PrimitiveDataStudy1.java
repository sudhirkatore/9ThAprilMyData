package TestPackage;

public class PrimitiveDataStudy1 {

	public static void main(String[] args) {
		
		// Primitive Data Types.(8 Data type)
		// I (Numeric + Non-decimal): - Ex: 80,85, 10, etc. 
		 
		 //1. byte 1 byte 
		// 2. short 2 bytes 
		 //3. int 4 bytes 
		//4. long 8 bytes 
		  
		//II (Numeric + decimal): - Ex: 22.5,22.8,6.4.... 
		 //5. float 4 byte 
		 //6. double 8 byte 
		
		//III Character: - Ex: A, B, X, Z. 
		 //7. char 2 byte 
		 
		//IV Conditional: - Ex: true, false. 
		 //8. boolean 1 bit
		
		byte Rollnum;
		Rollnum=127;
		System.out.println("Roll No= "+Rollnum);
		
		short Id;
		Id=12345;
		System.out.println("Id= "+Id);
		
		int PRN;
		PRN=1234567890;
		System.out.println("PRN= "+PRN);
		
		long A;
		A=123456789;
		System.out.println("A= "+A);
		
		float B;
		B=11.12345678901234567894556569531618881f;
		System.out.println("B= "+B);
		
		double C;
		C=12.123456789987456123d;
		System.out.println("C= "+C);
		
		
		
		
		
		

	}

}
